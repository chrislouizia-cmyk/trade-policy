create table if not exists public.instrument_catalog (
  id bigint generated by default as identity primary key,
  symbol text not null,
  display_name text not null,
  market_type text not null check (market_type in ('FOREX','FUTURES','STOCKS','ETFS','CRYPTO','INDEX','METALS')),
  category text not null default 'OTHER',
  exchange text,
  country text,
  sector text,
  base_currency text,
  quote_currency text,
  provider_symbol text,
  is_active boolean not null default true,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  unique(symbol, market_type)
);

create table if not exists public.custom_instruments (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  symbol text not null,
  display_name text not null,
  market_type text not null,
  provider_symbol text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  unique(user_id, symbol, market_type)
);

create table if not exists public.strategy_instruments (
  id uuid primary key default gen_random_uuid(),
  strategy_id uuid not null references public.strategy_profiles(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  symbol text not null,
  market_type text not null,
  provider_symbol text,
  sort_order integer not null default 0,
  enabled boolean not null default true,
  created_at timestamptz not null default now(),
  unique(strategy_id, symbol, market_type)
);

create table if not exists public.strategy_sessions (
  id uuid primary key default gen_random_uuid(),
  strategy_id uuid not null references public.strategy_profiles(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  session_code text not null,
  name text not null,
  timezone text not null default 'UTC',
  start_time time not null,
  end_time time not null,
  days smallint[] not null default array[1,2,3,4,5],
  allow_open_outside boolean not null default false,
  allow_hold_outside boolean not null default true,
  is_custom boolean not null default false,
  created_at timestamptz not null default now(),
  unique(strategy_id, session_code)
);

create table if not exists public.strategy_rules (
  id uuid primary key default gen_random_uuid(),
  strategy_id uuid not null references public.strategy_profiles(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  rule_key text not null,
  label text not null,
  enabled boolean not null default true,
  mandatory boolean not null default false,
  weight numeric not null default 0 check (weight >= 0 and weight <= 100),
  minimum_confidence integer not null default 0 check (minimum_confidence between 0 and 100),
  timeframe_role text not null default 'CONFIRMATION',
  configuration jsonb not null default '{}'::jsonb,
  sort_order integer not null default 0,
  unique(strategy_id, rule_key)
);

create table if not exists public.strategy_stop_limits (
  id uuid primary key default gen_random_uuid(),
  strategy_id uuid not null references public.strategy_profiles(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  instrument text not null,
  method text not null check (method in ('PIPS','POINTS','TICKS','PERCENT','ATR','STRUCTURAL')),
  maximum_value numeric not null check (maximum_value > 0),
  atr_multiplier numeric,
  configuration jsonb not null default '{}'::jsonb,
  unique(strategy_id, instrument)
);

create table if not exists public.strategy_exit_rules (
