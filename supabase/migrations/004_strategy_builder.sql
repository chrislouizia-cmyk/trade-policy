-- Ensure exactly one active profile per user while allowing zero during account setup.
create or replace function public.set_active_strategy(target_strategy_id uuid)
returns void
language plpgsql
security invoker
set search_path = public
as $$
begin
  if not exists (
    select 1 from public.strategy_profiles
    where id = target_strategy_id and user_id = auth.uid() and is_archived = false
  ) then
    raise exception 'Strategy not found or unavailable';
  end if;

  update public.strategy_profiles
  set is_default = false, updated_at = now()
  where user_id = auth.uid() and is_default = true;

  update public.strategy_profiles
  set is_default = true, updated_at = now()
  where id = target_strategy_id and user_id = auth.uid();
end;
$$;

grant execute on function public.set_active_strategy(uuid) to authenticated;

